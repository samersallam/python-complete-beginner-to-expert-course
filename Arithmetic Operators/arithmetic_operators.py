""" Lecture: Arithmetic Operators

    Outlines
    1- Addiction (+)
    2- Subtraction (-)
    3- Multiplication (*)
    4- Division (/)
    5- Modulus (%)
    6- Exponentiation (**)
    7- Floor Division (//)
"""


a = 10
b = 20

# 1- Addition (+)

c = a + b
print('a + b =', c)

# 2- Subtraction (-)

c = a - b
print('a - b =', c)

# 3- Multiplication (*)

c = a * b
print('a * b =', c)

# 4- Division (/)

c = a / b
print('a / b =', c)

# 5- Modulus (%)

c = a % b
print('a % b =', c)

# 6- Exponentiation (**)

c = a ** b
print('a ** b = ', c)

# 7- Floor Division (//)

c = a // b
print('a // b =', c)
