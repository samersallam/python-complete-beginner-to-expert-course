"""
1- Write a Python program to define the following variables
   course = 'Python', version = 3.8, duration = 120 (hour) using:
a.	Single-line statement
b.	Multi-statement in one line

"""
# a

course = 'Python'
version = 3.8
duration = 120  # hour
print('course = ', course)
print('version = ', version)
print('duration = ', duration)

# b
course = 'Python'; version = 3.8; duration = 120  # hour
print('course = ', course)
print('version = ', version)
print('duration = ', duration)

"""
2- Write a Python program to create a list with elements (1, 2, 43, 9) in two ways:
a.	In one line
b.	In multiple lines

"""
# a
list_1 = [1, 2, 43, 9]
print('list_1 in one line = ', list_1)

# b
list_1 = [1,
          2,
          43,
          9]
print('list_1 in multiple lines = ', list_1)